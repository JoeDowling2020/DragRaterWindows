package nokeynoshade;

public class ChallengesItem{
	private boolean won;
	private String description;
	private int id;
	private String type;

	public boolean isWon(){
		return won;
	}

	public String getDescription(){
		return description;
	}

	public int getId(){
		return id;
	}

	public String getType(){
		return type;
	}
}
