package nokeynoshade;

public class EpisodesItem{
	private int episodeInSeason;
	private int seasonId;
	private String airDate;
	private int id;
	private String title;

	public int getEpisodeInSeason(){
		return episodeInSeason;
	}

	public int getSeasonId(){
		return seasonId;
	}

	public String getAirDate(){
		return airDate;
	}

	public int getId(){
		return id;
	}

	public String getTitle(){
		return title;
	}
}
